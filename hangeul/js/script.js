function drop(ev){
  ev.preventDefault();
  var data = ev.dataTransfer.getData("text");
  var nodeCopy = document.getElementById(data).cloneNode(true); /*duplicate the item*/
  nodeCopy.id = "newId"; /* cannot use same id*/
  ev.target.appendChild(nodeCopy); /*duplicate the item*/
}


